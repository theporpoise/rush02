int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int dif;

	i = 0;
	dif = 0;
	while (s1[i] != '\0')
	{
		dif = s1[i] - s2[i];
		if (s2[i] == '\0' || (dif != 0))
		{
			printf("s2[i] is %c, s1[i] is %c", s2[i], s1[i]);
			printf("\ni is:%d\n", i);
			return (dif);
		}
		i++;
	}
	return (-(s2[i]));
}
